package com.eomcs.lang.ex04;

//# 한 문장에서 여러 개의 변수를 선언하기
//- 한 문장으로 같은 유형의 변수를 여러 개 선언할 수 있다.
//- 문법
//  데이터타입 변수1, 변수2, 변수3;
//
public class Exam0120 {

  public static void main(String[] args) {
    // 한 번에 한 개씩 변수를 선언할 수 있고,
    int i1; // int: 값을 저장할 메모리의 종류 데이터 타입(data type 자료형)이라 부른다.
    int i2; // i2: 메모리를 가리키는 이름, 변수(variables)라 부른다
    int i3; // 정수 값을 담을 메모리를 준비하고 그 메모리의 이름을 i라고 하자!

    // 콤마(,)를 사용하여 여러 개의 변수를 한 번에 선언할 수 있다.
    int j1, j2, j3;
  }
}
